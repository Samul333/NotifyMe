package com.huske.sharedexample;

public class upImage {

    public String name;
    public String url;


    public upImage() {

    }

    public upImage(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }



    public String getUrl() {
        return url;
    }



}
